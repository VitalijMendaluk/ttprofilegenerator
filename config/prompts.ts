export interface FormData {
  name: string;
  niche: string;
  nicheOther?: string;
  product: string;
  audience: string;
  result: string;
  link?: string;
}

export function buildPrompt(data: FormData): string {
  const niche = data.niche === "Інше" ? data.nicheOther || "Інше" : data.niche;
  const link = data.link || "не вказано";

  return `Ти — TikTok-маркетолог. Створи реальний, конкретний TikTok-профіль для продажів.

ДАНІ:
- Ім'я: ${data.name}
- Ніша: ${niche}
- Продукт: ${data.product}
- Аудиторія: ${data.audience}
- Результат клієнта: ${data.result}
- Посилання: ${link}

ЗАБОРОНЕНО ПИСАТИ (ці фрази — сміття):
❌ "я вчу", "я допомагаю", "я продаю", "підписуйся", "тут ти знайдеш"
❌ Просто ім'я + назва продукту без емоції чи результату
❌ Загальні слова: "якісно", "професійно", "ефективно"
❌ У nickname: просто "ім'я_ніша" або "ім'я_продукт"

ВИМОГИ:
- Nickname: коротке, запам'ятовується, з характером. Може бути грою слів, числом, емоцією. До 24 символів. Без пробілів.
- Bio: конкретний результат або провокаційне питання. Говорить мовою болю аудиторії. До 80 символів.
  Приклад хорошого біо: "Мінус 8 кг без дієт — або поверну гроші 🔥"
  Приклад хорошого біо: "Чоловіки зупиняються на вулиці? Ні? Тоді сюди👇"
- Header (3 рядки ім'я під аватаром): конкретно хто ти, для кого, що отримає. Кожен рядок окремо.
- CTA: одне речення яке вимовляють в кінці відео. Конкретна дія + причина зробити зараз.
- Ідея відео: назви формат (лайфхак/трансформація/помилки/день з мого життя), гачок першої секунди, суть контенту.

Поверни ТІЛЬКИ валідний JSON:
{
  "nickname": {
    "option1": "нікнейм без пробілів до 24 символів",
    "option2": "інший варіант нікнейму",
    "option3": "третій варіант нікнейму",
    "tip": "одне речення — чому ці нікнейми сильніші за просте ім'я"
  },
  "bio": {
    "text": "готовий текст біо до 80 символів",
    "tip": "який тригер або емоцію використано"
  },
  "header": {
    "line1": "рядок 1 до 30 символів",
    "line2": "рядок 2 до 30 символів",
    "line3": "рядок 3 до 30 символів",
    "tip": "як ці 3 рядки працюють разом"
  },
  "cta": {
    "text": "текст CTA для відео — 1 речення",
    "tip": "коли і як його вимовляти"
  },
  "link": {
    "recommendation": "що конкретно поставити в посилання для цієї ніші",
    "tip": "що зробити якщо посилання немає"
  },
  "first_video_idea": "формат відео + гачок першої секунди + про що говорити. 2-3 речення."
}
Мова: українська. Тільки JSON.`;
}
