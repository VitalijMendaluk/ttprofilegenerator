export interface FormData {
  name: string;
  niche: string;
  nicheOther?: string;
  product: string;
  audience: string;
  result: string;
  link?: string;
}

export function buildPrompt(data: FormData): string {
  const niche = data.niche === "Інше" ? data.nicheOther || "Інше" : data.niche;
  const link = data.link || "не вказано";

  return `Ти — топовий TikTok-маркетолог з досвідом 7+ років. Твоя задача — створити профіль, який з першого погляду чіпляє аудиторію і конвертує в продажі.

КОНТЕКСТ ЛЮДИНИ:
- Ім'я: ${data.name}
- Ніша: ${niche}
- Продукт/компанія: ${data.product}
- Цільова аудиторія: ${data.audience}
- Результат, який отримує клієнт: ${data.result}
- Посилання: ${link}

ВИМОГИ ДО ЯКОСТІ:
- Нікнейм: легко запам'ятати, легко знайти, відображає нішу або особистість. НЕ просто ім'я + ніша. Придумай щось з характером.
- Біо: це єдиний шанс зачепити. Має бути або провокаційно, або конкретна обіцянка, або емоційний тригер. НЕ "я вчу / я продаю". Говори мовою РЕЗУЛЬТАТУ і болю аудиторії.
- Шапка (ім'я у профілі): це те що бачать ПІД аватаркою. Має пояснювати ХТО ТИ і ДЛЯ КОГО за 3 секунди. Рядки мають бути логічно пов'язані.
- CTA: конкретний, з дієсловом, викликає бажання діяти прямо зараз.
- Ідея відео: реалістична, висока вірусність, відповідає ніші.

Поверни ТІЛЬКИ валідний JSON, без тексту до або після:
{
  "nickname": {
    "option1": "нікнейм 1 (до 24 символів, латиниця або кирилиця)",
    "option2": "нікнейм 2 (до 24 символів)",
    "option3": "нікнейм 3 (до 24 символів)",
    "tip": "коротко чому ці варіанти сильніші за просте ім'я"
  },
  "bio": {
    "text": "готовий текст біо (до 80 символів з емодзі)",
    "tip": "який психологічний тригер використано і чому це працює"
  },
  "header": {
    "line1": "рядок 1 — хто ти (до 30 символів)",
    "line2": "рядок 2 — результат або для кого (до 30 символів)",
    "line3": "рядок 3 — дія або обіцянка (до 30 символів)",
    "tip": "як ці 3 рядки разом створюють цілісний меседж"
  },
  "cta": {
    "text": "текст заклику до дії для завершення відео (1-2 речення)",
    "tip": "де і як вимовляти цей CTA для максимального ефекту"
  },
  "link": {
    "recommendation": "конкретна порада що поставити в посилання для цієї ніші",
    "tip": "якщо посилання немає — що зробити замість нього"
  },
  "first_video_idea": "конкретна ідея першого відео: формат + гачок + про що говорити (2-3 речення)"
}
Мова відповіді: українська.`;
}
